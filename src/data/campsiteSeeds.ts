// Helper: Recreation.gov opens bookings 6 months before check-in at 10am ET
function recGov6mo(checkInDate: string): string {
  const d = new Date(checkInDate)
  d.setMonth(d.getMonth() - 6)
  d.setUTCHours(14, 0, 0, 0) // 10am ET = 14:00 UTC
  return d.toISOString()
}

